def start(url):
    print(url)
    try:
        html = requests.get(url[:-1], headers=headers).content.decode('utf-8')
        # title =  etree.HTML(html).xpath('//*[@id="main_title"]')
        content = ''.join(etree.HTML(html).xpath('//*[@id="artibody"]/p/text()'))
    except:
        html = requests.get(url[:-1], headers=headers).content.decode('GBK')
        # title =  etree.HTML(html).xpath('//*[@id="main_title"]')
        content = ''.join(etree.HTML(html).xpath('//*[@id="artibody"]/p/text()'))
    with open('sina\\sina_content_4.txt', 'a', encoding='utf-8') as fe:
        fe.write(content + '\n')
    with open('sina\\log4.txt', 'w') as ft:
        ft.write(url)


def main(item):
    pool = threadpool.ThreadPool(30)
    tasks = threadpool.makeRequests(start, item)
    [pool.putRequest(req) for req in tasks]
    pool.wait()


if __name__ == '__main__':
    with open('sina_army_urls.txt', 'r', encoding='utf-8') as f:
        main(f.readlines())
